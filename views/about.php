<h2>About F4K</h2>
<p class="lead">Finance 4 Kids is an educational website based on one of CS50's problem sets. It is intended to teach students how to invest in stocks, and see their value increase or decrease. Placed in a world where they have $10,000 they have to make desicions to invest in stocks by looking at real time news and charts. To encourage the students, there is a leaderboard where each student will be ranked based on their portfolio's value plus their remaining cash.</p>
<h2>About the Creator</h2>
<p class="lead">Asanshay Gupta is a 11 year old 6th grader bent on making what was once a boring back-of-the-newspaper concept into kids lives as a competitive game. He lives in Atlanta, GA, and loves to bike, swim, and code. Asanshay loves web designing and especially loves integrating APIs into his websites. Sometimes, he creates random websites like factfiles and templates.</p>
